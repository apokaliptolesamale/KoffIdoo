class ItemList<T extends ItemModel> implements EntityModelList<T> {
  final List<T> items;

  ItemList({
    this.items = const [],
  });

  factory ItemList.fromStringJson(String strJson) =>
      ItemList.fromJson(json.decode(strJson));

  factory ItemList.fromJson(Map<String, dynamic> json) => ItemList(
        items: List<T>.from(json["items"].map((x) => ItemModel.fromJson(x))),
      );

  factory ItemList.fromEmpty() => ItemList(
        items: List<T>.from([].map((x) => ItemList.fromJson(x))),
      );

  Map<String, dynamic> toJson() => {
        "items": List<dynamic>.from(items.map((x) => x.toJson())),
      };

  @override
  EntityModelList<T> fromJson(Map<String, dynamic> json) {
    return ItemList.fromJson(json);
  }

  @override
  EntityModelList<T> fromStringJson(String strJson) {
    return ItemList.fromStringJson(strJson);
  }

  @override
  List<T> getList() => items;

  @override
  int get getTotal => getList().length;

  @override
  EntityModelList<T> add(T element) => fromList([element]);

  @override
  EntityModelList<T> fromList(List<T> list) {
    for (var element in list) {
      if (!items.contains(element)) items.add(element);
    }
    return this;
  }

  static Future<T> fromXmlServiceUrl<T>(
      String url,
      String parentTagName,
      Future<T> Function(XmlDocument doc, XmlElement el) process,
      Future<T> Function() onError) async {
    return EntityModelList.fromXmlServiceUrl(
        url, parentTagName, process, onError);
  }

  static Future<T> getJsonFromXMLUrl<T>(
      String url,
      Future<T> Function(XmlDocument result) process,
      Future<T> Function() onError) async {
    return EntityModelList.getJsonFromXMLUrl(url, process, onError);
  }
}

