  ItemModel({
    required this.idItem,
  }) : super(
          idItem: idItem,
        );

  factory ItemModel.fromJson(Map<String, dynamic> json) => ItemModel(
        idItem: json["id_item"],
      );

  factory ItemModel.fromXml(
          XmlElement element, ItemModel Function(XmlElement el) process) =>
      process(element);

  @override
