class UserNotificationList<T extends UserNotificationModel>
    implements EntityModelList<T> {
  final List<T> user_notifications;

  UserNotificationList({
    required this.user_notifications,
  });

  factory UserNotificationList.fromJson(Map<String, dynamic> json) =>
      UserNotificationList(
        user_notifications: List<T>.from(json["user_notifications"]
            .map((x) => UserNotificationModel.fromJson(x))),
      );

  factory UserNotificationList.fromStringJson(String strJson) =>
      UserNotificationList.fromJson(json.decode(strJson));

  @override
  int get getTotal => getList().length;

  @override
  EntityModelList<T> add(T element) => fromList([element]);

  @override
  EntityModelList<T> fromJson(Map<String, dynamic> json) {
    return UserNotificationList.fromJson(json);
  }

  @override
  EntityModelList<T> fromList(List<T> list) {
    for (var element in list) {
      if (!user_notifications.contains(element)) {
        user_notifications.add(element);
      }
    }
    return this;
  }

  @override
  EntityModelList<T> fromStringJson(String strJson) {
    return UserNotificationList.fromStringJson(strJson);
  }

  @override
  List<T> getList() => user_notifications;

  Map<String, dynamic> toJson() => {
        "user_notifications":
            List<dynamic>.from(user_notifications.map((x) => x.toJson())),
      };

  static Future<T> fromXmlServiceUrl<T>(
      String url,
      String parentTagName,
      Future<T> Function(XmlDocument doc, XmlElement el) process,
      Future<T> Function() onError) async {
    return EntityModelList.fromXmlServiceUrl(
        url, parentTagName, process, onError);
  }

  static Future<T> getJsonFromXMLUrl<T>(
      String url,
      Future<T> Function(XmlDocument result) process,
      Future<T> Function() onError) async {
    return EntityModelList.getJsonFromXMLUrl(url, process, onError);
  }
}

