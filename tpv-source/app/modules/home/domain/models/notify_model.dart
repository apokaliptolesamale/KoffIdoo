class NotifyList<T extends NotifyModel> implements EntityModelList<T> {
  final List<T> notifys;

  NotifyList({
    required this.notifys,
  });

  factory NotifyList.fromJson(Map<String, dynamic> json) => NotifyList(
        notifys: json.isNotEmpty
            ? List<T>.from(json["notifys"].map((x) => NotifyModel.fromJson(x)))
            : [],
      );

  factory NotifyList.fromStringJson(String strJson) =>
      NotifyList.fromJson(json.decode(strJson));

  @override
  int get getTotal => getList().length;

  @override
  EntityModelList<T> add(T element) => fromList([element]);

  @override
  EntityModelList<T> fromJson(Map<String, dynamic> json) {
    return NotifyList.fromJson(json);
  }

  @override
  EntityModelList<T> fromList(List<T> list) {
    for (var element in list) {
      if (!notifys.contains(element)) notifys.add(element);
    }
    return this;
  }

  @override
  EntityModelList<T> fromStringJson(String strJson) {
    return NotifyList.fromStringJson(strJson);
  }

  @override
  List<T> getList() => notifys;

  Map<String, dynamic> toJson() => {
        "notifys": List<dynamic>.from(notifys.map((x) => x.toJson())),
      };

  static Future<T> fromXmlServiceUrl<T>(
      String url,
      String parentTagName,
      Future<T> Function(XmlDocument doc, XmlElement el) process,
      Future<T> Function() onError) async {
    return EntityModelList.fromXmlServiceUrl(
        url, parentTagName, process, onError);
  }

  static Future<T> getJsonFromXMLUrl<T>(
      String url,
      Future<T> Function(XmlDocument result) process,
      Future<T> Function() onError) async {
    return EntityModelList.getJsonFromXMLUrl(url, process, onError);
  }
}

